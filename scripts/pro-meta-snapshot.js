// One-time snapshot of a pro tournament's hero draft stats from OpenDota,
// written to js/pro-meta.js as static data for the Heroes tab comparison graph.
// Rerun (and commit the result) to refresh — e.g. after the tournament ends:
//   node scripts/pro-meta-snapshot.js

const LEAGUE_ID = 19785;
const TOURNAMENT_NAME = 'Esports World Cup 2026';

const fs = require('fs');
const path = require('path');
const sleep = ms => new Promise(r => setTimeout(r, ms));

async function getJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.json();
}

async function main() {
  const matches = await getJson(`https://api.opendota.com/api/leagues/${LEAGUE_ID}/matches`);
  console.log(`${TOURNAMENT_NAME}: ${matches.length} matches`);

  const heroes = {}; // heroId -> {picks, bans, wins}
  let drafted = 0;
  for (let i = 0; i < matches.length; i++) {
    process.stdout.write(`\r  fetching match ${i + 1}/${matches.length}`);
    let md;
    try { md = await getJson(`https://api.opendota.com/api/matches/${matches[i].match_id}`); }
    catch (e) { await sleep(2000); i--; continue; }
    await sleep(1100);
    if (!md.picks_bans || !md.picks_bans.length) continue;
    drafted++;
    md.picks_bans.forEach(pb => {
      const s = heroes[pb.hero_id] || (heroes[pb.hero_id] = { picks: 0, bans: 0, wins: 0 });
      if (pb.is_pick) {
        s.picks++;
        if ((pb.team === 0) === md.radiant_win) s.wins++;
      } else {
        s.bans++;
      }
    });
  }
  console.log(`\n  drafted games: ${drafted}`);

  const out = {
    tournament: TOURNAMENT_NAME,
    leagueId: LEAGUE_ID,
    snapshotDate: new Date().toISOString().slice(0, 10),
    games: drafted,
    heroes,
  };
  const file = path.join(__dirname, '..', 'js', 'pro-meta.js');
  fs.writeFileSync(file, `// Generated by scripts/pro-meta-snapshot.js — do not edit by hand\nconst PRO_META = ${JSON.stringify(out, null, 2)};\n`);
  console.log(`written: ${file}`);
}

main().catch(e => { console.error(e); process.exit(1); });
